 export function generateUUID() {
  let uuid = '';
  const characters = '0123456789abcdef';

  for (let i = 0; i < 36; i++) {
    if (i === 8 || i === 13 || i === 18 || i === 23) {
      uuid += '-';
    } else if (i === 14) {
      uuid += '4'; // Version 4 UUID
    } else if (i === 19) {
      uuid += characters[(Math.random() * 4 | 8)];
    } else {
      uuid += characters[(Math.random() * 16) | 0];
    }
  }

  return uuid;
}